package juddi;

public class JuddiProperties{
  static String authorizedName;
  static String publisherName;
  static String businessName;
  static String serviceName;
  static String serviceLang;
  static String serviceDesc;
  static String serviceKeyWord;
  static String wsdlURL;
  static String accessPointType;
  
  public void setAuthorizedName(String authorizedName){
    this.authorizedName=authorizedName;
  }
  public String getAuthorizedName(){
    return authorizedName;
  }   
  
  public void setPublisherName(String publisherName){
    this.publisherName=publisherName;
  }
  public String getPublisherName(){
    return publisherName;
  }  
  
  public void setBusinessName(String businessName){
    this.businessName=businessName;
  }
  public String getBusinessName(){
    return businessName;
  }  
  
  public void setServiceName(String serviceName){
    this.serviceName=serviceName;
  }
  public String getServiceName(){
    return serviceName;
  }  
  
  public void setServiceLang(String serviceLang){
    this.serviceLang=serviceLang;
  }
  public String getServiceLang(){
    return serviceLang;
  }  
  
  public void setServiceDesc(String serviceDesc){
    this.serviceDesc=serviceDesc;
  }
  public String getServiceDesc(){
    return serviceDesc;
  }  
  
  public void setServiceKeyWord(String serviceKeyWord){
    this.serviceKeyWord=serviceKeyWord;
  }
  public String getServiceKeyWord(){
    return serviceKeyWord;
  }  
  
  public void setWsdlURL(String wsdlURL){
    this.wsdlURL=wsdlURL;
  }
  public String getWsdlURL(){
    return wsdlURL;
  }  
  
  public void setAccessPointType(String accessPointType){
    this.accessPointType=accessPointType;
  }
  public String getAccessPointType(){
    return accessPointType;
  }  
}  
  